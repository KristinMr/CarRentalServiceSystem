package util;

public class Car {

    private String carID;
    private String carNumber;
    private String carBrand;
    private String carModel;
    private String carRent;
    private String carState;
    private String carPicture;
    private String carInfo;
    private int carRecycleBin;

    public String getCarID() {
        return carID;
    }

    public void setCarID(String carID) {
        this.carID = carID;
    }

    public String getCarNumber() {
        return carNumber;
    }

    public void setCarNumber(String carNumber) {
        this.carNumber = carNumber;
    }

    public String getCarBrand() {
        return carBrand;
    }

    public void setCarBrand(String carBrand) {
        this.carBrand = carBrand;
    }

    public String getCarModel() {
        return carModel;
    }

    public void setCarModel(String carModel) {
        this.carModel = carModel;
    }

    public String getCarRent() {
        return carRent;
    }

    public void setCarRent(String carRent) {
        this.carRent = carRent;
    }

    public String getCarState() {
        return carState;
    }

    public void setCarState(String carState) {
        this.carState = carState;
    }

    public String getCarPicture() {
        return carPicture;
    }

    public void setCarPicture(String carPicture) {
        this.carPicture = carPicture;
    }

    public String getCarInfo() {
        return carInfo;
    }

    public void setCarInfo(String carInfo) {
        this.carInfo = carInfo;
    }

    public int getCarRecycleBin() {
        return carRecycleBin;
    }

    public void setCarRecycleBin(int carRecycleBin) {
        this.carRecycleBin = carRecycleBin;
    }
}
